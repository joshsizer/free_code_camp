const bubbleSort = require("./bubble_sort")

test("bubbleSort([873, -3, -988, -49, -916, -552, -966, 830, -389, -539])", () => {
    expect(bubbleSort([873, -3, -988, -49, -916, -552, -966, 830, -389, -539])).toEqual([-988, -966, -916, -552, -539, -389, -49, -3, 830, 873])
})

test("bubbleSort([-672, 139, -537, 832, 968, 625, -907, 683, -401, 728])", () => {
    expect(bubbleSort([-672, 139, -537, 832, 968, 625, -907, 683, -401, 728])).toEqual([-907, -672, -537, -401, 139, 625, 683, 728, 832, 968])
})

